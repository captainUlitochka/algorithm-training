package com.company;

public class ArrayPrinter {
    public static void print(int[] arrayToPrint) {
        for (int i = 0; i< arrayToPrint.length; i++) {
            System.out.println(arrayToPrint[i]);
        }
    }

    public static void print(char[] arrayToPrint) {
        for (int i = 0; i< arrayToPrint.length; i++) {
            System.out.println(arrayToPrint[i]);
        }
    }
}
